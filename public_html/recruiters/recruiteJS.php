<script type="text/javascript">

function ChangeDIV(divName)
{
	divName = divName + "desc";
	var divOBJ = document.getElementById(divName);
	if (divOBJ.style.display == "none")
		divOBJ.style.display = "block";
	else
		divOBJ.style.display = "none";
}


</script>
